function find_zipcode($str) {
  global $cityarea_data;
  $str = str_replace(array(' ', '/'), '', $str);
  foreach ($cityarea_data as $city => $zipcode) {
    if (strpos($str, $city) !== FALSE) return $zipcode;
  }
  return 0;
}

